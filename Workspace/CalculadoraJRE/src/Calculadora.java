
public class Calculadora {

	public static void main(String[] args) {	
		
		/*NÚMEROS PARA A OPERAÇÃO*/
		double n1 = 5;
		double n2 = 2;
		
		/*RESULTADOS DE CADA TIPO DE CONTA*/
		double resulS;
		double resulSu;
		double resulM;
		double resulD;
		
		resulS = n1 + n2;
		System.out.println("O resultado da soma é: " + resulS);
		
		resulSu = n1 - n2;
		System.out.println("O resultado da subtração é: " + resulSu);
		
		resulM = n1 * n2;
		System.out.println("O resultado da multiplicação: " + resulM);
		
		resulD = n1 / n2;
		System.out.println("O resultado da divisão é: " + resulD);
		
		
		
	}

}

